    private void btnsimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsimpanActionPerformed
        // TODO add your handling code here:
        String jd=judul.getText();
        String id=idbuku.getText();
        String pn=penulis.getText();
        String pb=penerbit.getText();
        String thn=(String) cbtahun.getSelectedItem();
        String jenis="";
        if(rdpelajaran.isSelected()){
            jenis=rdpelajaran.getText();
        }else if(rdnovel.isSelected()){
            jenis=rdnovel.getText();
        }else if(rdbiografi.isSelected()){
            jenis=rdbiografi.getText();
        }else if(rdhobi.isSelected()){
            jenis=rdbiografi.getText();
        }else if(rdkomik.isSelected()){
            jenis=rdkomik.getText();
        }else if(rdlain.isSelected()){
            jenis=rdlain.getText();
        }
        int hg=Integer.parseInt(harga.getText());
        try{
            Statement st=(Statement)n.GetConnection().createStatement();
            String sql="insert into buku (id,judul,penulis,penerbit,tahun,jenis,harga) VALUES('"+id+"','"+jd+"','"+pn+"','"+pb+"','"+thn+"','"+jenis+"',"+hg+");";
            st.executeUpdate(sql);
            JOptionPane.showMessageDialog(null, "BERHASIL DI SAVE","Informasi",JOptionPane.INFORMATION_MESSAGE);
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, "GAGAL DI SAVE","Informasi",JOptionPane.ERROR_MESSAGE);
        }
        
    }//GEN-LAST:event_btnsimpanActionPerformed

