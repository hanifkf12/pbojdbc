    private void btntampilActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btntampilActionPerformed
        // TODO add your handling code here:
        try{
            Statement stt=(Statement)n.GetConnection().createStatement();
            String sql="SELECT * FROM buku;";
            ResultSet st=stt.executeQuery(sql);
        
            while(st.next()){
                String jd=st.getString("judul");
                String id=st.getString("id");
                String pn=st.getString("penulis");
                String pb=st.getString("penerbit");
                String thn=st.getString("tahun");
                String jenis=st.getString("jenis");
                int harga=Integer.parseInt(st.getString("harga"));   
                DefaultTableModel model=(DefaultTableModel)tbuku.getModel();
                model.addRow(new Object[]{
                    id,
                    jd,
                    pn,
                    pb,
                    thn,
                    jenis,
                    harga
                });
               }
            }
        catch(Exception e){
            System.out.println("Gagal");
        }
    }//GEN-LAST:event_btntampilActionPerformed

